#include "3-calc.h"

/**
 * op_add - calculates sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Sum of two integers (a & b)
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates difference of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Diference of two integers (a & b)
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Product of two integers (a & b)
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates quotient of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Quotient of two integers (a & b)
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates modulo of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Modulo of two integers (a & b)
*/

int op_mod(int a, int b)
{
	return (a % b);
}
